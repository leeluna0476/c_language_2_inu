#include <stdio.h>

#define WIDTH 8
#define HEIGHT 8

void print_letter(char data[8], const char charset[2]) {
	for (int i = 0; i < HEIGHT; ++i) {
		for (int j = WIDTH - 1; j >= 0; --j) {
			if ((data[i] >> j) & 1)
				printf("%c", charset[0]);
			else
				printf("%c", charset[1]);
		}
		printf("\n");
	}
}

int main() {
//	int zero[2] = {0x3c428181, 0x8181423c}; // 8*8 character
	char digits[10][8] = {
		{ 0x3c, 0x7e, 0xc3, 0xc3, 0xc3, 0xc3, 0x7e, 0x3c },
		{ 0x0c, 0x3c, 0x6c, 0xcc, 0x0c, 0x0c, 0xff, 0xff },
		{ 0x3c, 0xff, 0xc3, 0x03, 0x06, 0x0c, 0xff, 0xff },
		{ 0x3c, 0xff, 0xc3, 0x07, 0x3e, 0x03, 0xc3, 0x7e },
		{ 0x1e, 0x36, 0x66, 0xc6, 0xff, 0xff, 0x06, 0x06 },
		{ 0xff, 0xff, 0xc0, 0xff, 0xff, 0x03, 0xff, 0xff },
		{ 0xff, 0xff, 0xc0, 0xff, 0xff, 0xc3, 0xff, 0xff },
		{ 0xff, 0xff, 0xc3, 0x03, 0x1c, 0x18, 0x18, 0x18 },
		{ 0x7e, 0xff, 0xc3, 0xff, 0x7e, 0xc3, 0xff, 0x7e },
		{ 0x7e, 0xff, 0xc3, 0xff, 0x7e, 0x06, 0x06, 0x06 }
	};

	for (int i = 0; i < 10; ++i) {
		print_letter(digits[i], "* ");
		printf("\n");
	}
}



